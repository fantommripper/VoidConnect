//! События хранилища о качестве работы пиров — питают систему репутации.
//!
//! `StorageManager` шлёт их в опциональный канал (см. `set_event_sink`), а
//! backend перекладывает в `void-reputation`. Сам storage о репутации не знает
//! — связь односторонняя через нейтральный тип, без зависимости на крейт
//! репутации.

use void_core::identity::NodeId;

/// Событие о переданном пиром чанке.
#[derive(Debug, Clone)]
pub enum ChunkEvent {
    /// Пир отдал валидный чанк (прошёл проверку SHA-256).
    Valid { peer: NodeId, size_bytes: i64 },
    /// Пир отдал битый чанк (хэш не совпал).
    Bad { peer: NodeId },
}
